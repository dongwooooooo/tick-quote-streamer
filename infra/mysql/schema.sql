-- 실시간 주식 시세 스트리밍 시스템 DB 스키마

-- 종목 정보 테이블
CREATE TABLE IF NOT EXISTS stocks (
    stock_code VARCHAR(10) PRIMARY KEY,
    stock_name VARCHAR(100) NOT NULL,
    market_type VARCHAR(10) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    INDEX idx_market_type (market_type),
    INDEX idx_stock_name (stock_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='종목 기본 정보';

-- 실시간 시세 데이터 테이블
CREATE TABLE IF NOT EXISTS quote_data (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    stock_code VARCHAR(10) NOT NULL COMMENT '종목코드',
    price DECIMAL(15,2) NOT NULL COMMENT '현재가',
    volume BIGINT NOT NULL COMMENT '거래량',
    change_amount DECIMAL(15,2) COMMENT '전일 대비 변동금액',
    change_rate DECIMAL(8,4) COMMENT '전일 대비 변동률(%)',
    high_price DECIMAL(15,2) COMMENT '고가',
    low_price DECIMAL(15,2) COMMENT '저가',
    open_price DECIMAL(15,2) COMMENT '시가',
    trade_time TIMESTAMP NOT NULL COMMENT '체결시간',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    FOREIGN KEY (stock_code) REFERENCES stocks(stock_code),
    INDEX idx_stock_code_time (stock_code, trade_time),
    INDEX idx_trade_time (trade_time),
    INDEX idx_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='실시간 시세 데이터';

-- 실시간 호가 마스터 테이블 (하나의 시점에 대한 전체 호가 정보를 대표)
CREATE TABLE IF NOT EXISTS orderbooks (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    stock_code VARCHAR(10) NOT NULL COMMENT '종목코드',
    quote_time TIMESTAMP(6) NOT NULL COMMENT '호가 수신 시간 (마이크로초까지)',
    sequence_number BIGINT COMMENT '데이터 수신 순서 (중복/순서 검증용)',
    total_bid_volume BIGINT COMMENT '총 매수호가 잔량',
    total_ask_volume BIGINT COMMENT '총 매도호가 잔량',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    FOREIGN KEY (stock_code) REFERENCES stocks(stock_code),
    INDEX idx_stock_code_time (stock_code, quote_time),
    INDEX idx_sequence (sequence_number),
    INDEX idx_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='실시간 호가 정보 마스터';

-- 실시간 호가 레벨 상세 테이블 (10단계 호가 정보를 각각의 행으로 저장)
CREATE TABLE IF NOT EXISTS orderbook_levels (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    orderbook_id BIGINT NOT NULL COMMENT '호가 마스터 ID',
    order_type VARCHAR(4) NOT NULL COMMENT '호가 타입 (BID, ASK)',
    price_level INT NOT NULL COMMENT '호가 순서 (1~10)',
    price DECIMAL(15,2) NOT NULL COMMENT '호가',
    volume BIGINT NOT NULL COMMENT '호가 잔량',

    FOREIGN KEY (orderbook_id) REFERENCES orderbooks(id) ON DELETE CASCADE,
    INDEX idx_orderbook_id (orderbook_id),
    INDEX idx_orderbook_type_level (orderbook_id, order_type, price_level)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='실시간 호가 상세 레벨';

-- 사용자 알림 조건 테이블
CREATE TABLE IF NOT EXISTS notification_conditions (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50) NOT NULL COMMENT '사용자ID',
    stock_code VARCHAR(10) NOT NULL COMMENT '종목코드',
    condition_type VARCHAR(20) NOT NULL COMMENT '조건타입 (PRICE_ABOVE, PRICE_BELOW, VOLUME_ABOVE)',
    target_value DECIMAL(15,2) NOT NULL COMMENT '목표값',
    current_value DECIMAL(15,2) COMMENT '현재값 (조건 평가시 저장)',
    is_active BOOLEAN DEFAULT TRUE COMMENT '활성화 여부',
    triggered_at TIMESTAMP NULL COMMENT '조건 트리거 시간',
    description VARCHAR(255) COMMENT '조건 설명',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    FOREIGN KEY (stock_code) REFERENCES stocks(stock_code),
    INDEX idx_user_id (user_id),
    INDEX idx_stock_code_active (stock_code, is_active),
    INDEX idx_condition_type (condition_type),
    INDEX idx_is_active (is_active)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='사용자 알림 조건';

-- 알림 이력 테이블
CREATE TABLE IF NOT EXISTS notification_history (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(50) NOT NULL COMMENT '사용자ID',
    stock_code VARCHAR(10) NOT NULL COMMENT '종목코드',
    condition_id BIGINT NOT NULL COMMENT '알림 조건 ID',
    condition_type VARCHAR(20) NOT NULL COMMENT '조건타입',
    target_value DECIMAL(15,2) NOT NULL COMMENT '목표값',
    triggered_value DECIMAL(15,2) NOT NULL COMMENT '트리거된 값',
    message VARCHAR(500) NOT NULL COMMENT '알림 메시지',
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING' COMMENT '전송 상태 (PENDING, SENT, FAILED, RETRY)',
    retry_count INT DEFAULT 0 COMMENT '재시도 횟수',
    sent_at TIMESTAMP NULL COMMENT '전송 완료 시간',
    error_message VARCHAR(1000) COMMENT '오류 메시지',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    FOREIGN KEY (condition_id) REFERENCES notification_conditions(id),
    FOREIGN KEY (stock_code) REFERENCES stocks(stock_code),
    INDEX idx_user_id_created (user_id, created_at),
    INDEX idx_stock_code_created (stock_code, created_at),
    INDEX idx_condition_id (condition_id),
    INDEX idx_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='알림 전송 이력';

-- 핫토픽 정보 테이블
CREATE TABLE IF NOT EXISTS hot_stocks (
    stock_code VARCHAR(10) PRIMARY KEY,
    reason VARCHAR(50) COMMENT '핫토픽 선정 이유 (e.g., HIGH_TRAFFIC, VOLUME_SPIKE)',
    score DECIMAL(10, 4) COMMENT '핫토픽 점수 (기준 값)',
    designated_at TIMESTAMP NOT NULL COMMENT '핫토픽 지정 시간',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

    FOREIGN KEY (stock_code) REFERENCES stocks(stock_code) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='실시간 핫토픽 종목 정보';

